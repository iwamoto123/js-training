import { knock } from "../src/knock052.js";

describe("No. 52 閏年", () => {
  test("入力例1: 400で割り切れる年 (2000年)", () => {
    expect(knock(2000)).toBe("2000年は閏年です");
  });

  test("入力例2: 100で割り切れる年 (2100年)", () => {
    expect(knock(2100)).toBe("2100年は閏年ではありません");
  });

  test("入力例3: 4で割り切れる年 (2024年)", () => {
    expect(knock(2024)).toBe("2024年は閏年です");
  });

  test("4で割り切れない年 (2023年)", () => {
    expect(knock(2023)).toBe("2023年は閏年ではありません");
  });

  test("400で割り切れる年 (1600年)", () => {
    expect(knock(1600)).toBe("1600年は閏年です");
  });

  test("100で割り切れるが400で割り切れない年 (1900年)", () => {
    expect(knock(1900)).toBe("1900年は閏年ではありません");
  });

  test("4で割り切れる通常の年 (2020年)", () => {
    expect(knock(2020)).toBe("2020年は閏年です");
  });

  test("負の年 (紀元前400年)", () => {
    expect(knock(-400)).toBe("-400年は閏年です");
  });

  test("負の年で閏年でない場合 (紀元前2023年)", () => {
    expect(knock(-2023)).toBe("-2023年は閏年ではありません");
  });
});
